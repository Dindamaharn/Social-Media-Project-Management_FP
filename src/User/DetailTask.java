    private void fetchTaskData() {
        try {
            Connection conn = Database.DatabaseConnection.getConnection();
            String sql = """
                        WITH latest_status AS (
                             SELECT st.*,
                                    ROW_NUMBER() OVER (PARTITION BY tasks_id ORDER BY created_at DESC) AS rn
                             FROM status_tracks st
                         )
                         
                         SELECT 
                             t.id,
                             t.name AS task_name,
                             t.desc,
                             t.point,
                             t.deadline,
                             p.name AS project_name,
                             a.name AS assignee_name,
                             adm.name AS admin_name,
                             ls.status AS status_name
                         FROM tasks t
                         JOIN projects p ON t.projects_id = p.id
                         JOIN assignees a ON t.assignees_id = a.id
                         JOIN admins adm ON t.admins_id = adm.id
                         LEFT JOIN latest_status ls ON ls.tasks_id = t.id AND ls.rn = 1
                         WHERE t.id = ?;
                                                              
                         """;
            PreparedStatement stmt = conn.prepareStatement(sql);
            stmt.setInt(1, taskId);
            this.rs = stmt.executeQuery();
            this.bindTaskData();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error loading tasks: " + e.getMessage());
            e.printStackTrace();
        }
    }

    private void bindTaskData() {
        try {
            while (rs.next()) {
                rs.getInt("id");
                txtTaskName.setText(rs.getString("task_name"));
                txtDesc.setText(rs.getString("desc"));
                txtDueDate.setText(rs.getString("deadline"));
                rs.getString("assignee_name");
                txtAdminName.setText(rs.getString("admin_name"));
                txtStatus.setText(rs.getString("status_name"));
            }
            fetchActivityLogData();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error loading tasks: " + e.getMessage());
            e.printStackTrace();
        }
    }

